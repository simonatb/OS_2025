##Зад. 25 2017-IN-03 Напишете скрипт, който извежда името на потребителския акаунт, в чиято home директория има най-скоро променен обикновен файл и кой е този файл. Напишете скрипта с подходящите
##проверки, така че да бъде валиден инструмент.

#!/bin/bash

result=$(find /home -type f -exec stat -c "%U %Y" {} + 2>/dev/null | sort -k 2 | tail -n 1 | cut -d ' ' -f 1 )
echo "$result"
